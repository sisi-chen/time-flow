package com.entity;

public class LoginMessage {
		String no;
		String password;
		public String getNo(){
			return no;
		}
		
		public void  setNo(String no){
			this.no=no;
		}
		
		public String getPassword(){
			return password;
		}
		
		public void  setPassword(String code){
			this.password=code;
		}
		
}
